/**
 * Arithmetic operators
 * @link https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Operators#Arithmetic_operators
 */

let a = 15;
let b = "4";
let c = 3.2;

console.log(`let a: ${a} (${typeof a})`);
console.log(`let b: ${b} (${typeof b})`);
console.log(`let c: ${c} (${typeof c})`);

let result1 = a - b;
console.log("Result: ", result1, typeof result1);

let result2 = a + b;
console.log("Result: ", result2, typeof result2);

let result3 = a * b;
console.log("Result: ", result3, typeof result3);

let result4 = a / b;
console.log("Result: ", result4, typeof result4);
